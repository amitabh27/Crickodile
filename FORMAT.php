                  <p align="justify" class="style17">* FORMAT: </p>
                  <p align="justify" class="style15">From its inception until the 2001-02 season, the teams were grouped geographically into four or five zones &ndash; North, West, East, and South, with Central added in 1952-53. Initial matches were played within the zones on a&nbsp;knock-out&nbsp;basis until 1956-57, and thereafter on a&nbsp;league&nbsp;basis, to determine a winner. Then, the individual zone winners competed in a knock-out tournament, leading to a final which decided the winner of the Ranji Trophy. In the 1970-71 season, the knock-out stage was expanded to the top two teams from each zone, a total of ten qualifying teams. This was expanded again to the top three from each zone in 1992-93, a total of fifteen qualifying teams; between 1996&ndash;97 and 1999-2000, the fifteen qualifying teams competed in a secondary group stage, with three groups of five teams, and the top two from each group qualifying for the knock-out stage; in all other years, a full fifteen-team knock-out tournament was held.</p>
                  <p align="justify" class="style15">The format was changed in&nbsp;2002&ndash;03 season&nbsp;with the zonal system abandoned and a two-division structure was adopted &ndash; the Elite Group, containing fifteen teams, and the Plate Group, containing the rest. Each group has two sub-groups who play a round-robin; the top two from each sub-group then contest a knock-out tournament to determine the winner. The team which finishes last in each Elite sub-group is relegated, and both Plate Group finalists are promoted, for the following season. For the&nbsp;2006&ndash;07 season, the divisions were re-labelled the Super League and Plate League respectively.</p>
                  <p align="justify" class="style15">In the 2008&ndash;09 season, the current format was adopted to allow Plate Group teams to contest the Ranji Trophy. The top two from each Plate sub-group contest semi-finals; the winners of these two matches then join the top three from each Super League sub-group in an eight-team knock-out tournament. The winner of this knock-out tournament then wins the Ranji Trophy. In 2010-11 season, history was created when a plate group team &quot;Rajasthan&quot; not only entered into the Elite Group but went on to win their maiden Ranji Trophy final.</p>
                  <p align="justify" class="style15">From the 2012-13 season, the format was revamped completely. The Super and plate groups were cancelled and in place, a format with A, B, C group was created. Three teams from group A&amp;B proceeded to knockouts and 2 from group C. The lowest placed team of Group A were relegated to Group B next time, lowest placed of group B relegated to C . Top team of Group C promoted to group B and top team of group B promoted to group A. The knockout format remains same except that, to get an outright result from final an extra sixth day is available instead of the restricted five days</p>
                  <p align="justify" class="style15">Knock-out matches in the Ranji Trophy are decided on the first innings result if the final result is a draw.</p>
                  <p align="justify" class="style15">The matches are a 4 day match format except for the knockout matches which are played for five days just as a regular test match.</p>
