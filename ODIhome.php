              <p class="style34"><u>INTRODUCTION:</u></p>
              <blockquote>
<blockquote>
  <p align="left" class="style40">A&nbsp;<strong>One Day International (ODI)</strong>&nbsp;is a form of&nbsp;<a href="http://en.wikipedia.org/wiki/Limited_overs_cricket" title="Limited overs cricket">limited overs cricket</a>, played between two teams with international status, in which each team faces a fixed number of&nbsp;<a href="http://en.wikipedia.org/wiki/Over_(cricket)" title="Over (cricket)">overs</a>, usually fifty. The&nbsp;<a href="http://en.wikipedia.org/wiki/Cricket_World_Cup" title="Cricket World Cup">Cricket World Cup</a>&nbsp;is played in this format. One Day International matches are also called &quot;Limited Overs Internationals (LOI)&quot;, although this generic term may also refer to&nbsp;<a href="http://en.wikipedia.org/wiki/Twenty20_International" title="Twenty20 International">Twenty20 International</a>matches.</p>
</blockquote>
<p align="left" class="style40">&nbsp;</p>
                <p align="left" class="style41">* HISTORY: </p>
                <blockquote>
                  <p align="left" class="style42">The international one-day game is a late twentieth-century development. The first ODI was played on 5 January 1971 between Australia and England at the<a href="http://en.wikipedia.org/wiki/Melbourne_Cricket_Ground" title="Melbourne Cricket Ground">Melbourne Cricket Ground</a>. When the first three days of the third&nbsp;<a href="http://en.wikipedia.org/wiki/Test_cricket" title="Test cricket">Test</a>&nbsp;were washed out officials decided to abandon the match and, instead, play a one-off one day game consisting of 40 eight-ball overs per side. Australia won the game by 5 wickets.</p>
                  <p align="left" class="style42">&nbsp;</p>
                  <p align="center" class="style43"><a href="images/ODI/history/index.htm">IMAGE GALLERY:</a></p>
