                  <p align="justify" class="style43">&nbsp; </p>
                  <p align="justify" class="style42">In the late 1970s,&nbsp;<a href="http://en.wikipedia.org/wiki/Kerry_Packer" title="Kerry Packer">Kerry Packer</a>&nbsp;established the rival&nbsp;<a href="http://en.wikipedia.org/wiki/World_Series_Cricket" title="World Series Cricket">World Series Cricket</a>(WSC) competition, and it introduced many of the features of One Day International cricket that are now commonplace, including coloured uniforms, matches played at night under floodlights with a white ball and dark sight screens, and, for television broadcasts, multiple camera angles, effects microphones to capture sounds from the players on the pitch, and on-screen graphics. The first of the matches with coloured uniforms was the WSC Australians in wattle gold versus WSC West Indians in coral pink, played at&nbsp;<a href="http://en.wikipedia.org/wiki/VFL_Park" title="VFL Park">VFL Park</a>&nbsp;in&nbsp;<a href="http://en.wikipedia.org/wiki/Melbourne" title="Melbourne">Melbourne</a>&nbsp;on 17 January 1979. This led not only to Kerry Packer's&nbsp;<a href="http://en.wikipedia.org/wiki/Channel_9" title="Channel 9">Channel 9</a>&nbsp;getting the TV rights to cricket in Australia but also led to players worldwide being paid to play, and becoming international professionals, no longer needing jobs outside of cricket.</p>
                  <p align="justify" class="style42">&nbsp;</p>
                  <p align="center" class="style45"><u>RULES:</u></p>
                  </blockquote>
                <p align="justify">&nbsp;</p>
                <p align="justify" class="style44">In the main the&nbsp;<a href="http://en.wikipedia.org/wiki/Laws_of_cricket" title="Laws of cricket">Laws of cricket</a>&nbsp;apply. However, in ODIs, each team gets to bat only a fixed number of&nbsp;<a href="http://en.wikipedia.org/wiki/Over_(cricket)" title="Over (cricket)">overs</a>. In the early days</p>
                <p align="left" class="style44">of ODI cricket, the number of overs was generally 60 overs per side, and matches were also played with 40, 45 or 55 overs per side, but now it has been uniformly fixed at 50 overs.</p>
                <p align="justify" class="style44">Simply stated, the game works as follows:</p>
                <p align="justify" class="style44">-&gt; An ODI is contested by 2 teams of 11 players each.</p>
                <div align="justify" class="style44">
                  <p>-&gt; The Captain of the side winning the&nbsp;<a href="http://en.wikipedia.org/wiki/Coin_flipping" title="Coin flipping">toss</a>&nbsp;chooses to either&nbsp;<a href="http://en.wikipedia.org/wiki/Batting_(cricket)" title="Batting (cricket)">bat</a>&nbsp;or&nbsp;<a href="http://en.wikipedia.org/wiki/Bowling_(cricket)" title="Bowling (cricket)">bowl</a>&nbsp;(field) first</p>
                  <p align="left">-&gt;  The team batting first sets the target score in a single&nbsp;<a href="http://en.wikipedia.org/wiki/Innings" title="Innings">innings</a>. The innings lasts until the batting side is &quot;all out&quot; (i.e., 10 of the 11 batting players are &quot;out&quot;) or all of the first side's allotted overs are completed. </p>
                  <p align="left">-&gt; Each bowler is restricted to bowling a maximum of 10 overs (fewer in the case of rain-reduced matches and in any event generally no more than one fifth or 20% of the total overs per innings). Due to this, each team must comprise at least five competent bowlers (either dedicated bowlers or all-rounders).</p>
                  <p align="left">-&gt; The team batting second tries to score more than the target score in order to win the match. Similarly, the side bowling second tries to bowl out the second team or make them exhaust their overs before they reach the target score in order to win.</p>
                  <p align="left">-&gt; If the number of runs scored by both teams is equal when the second team loses all of its&nbsp;<a href="http://en.wikipedia.org/wiki/Wicket" title="Wicket">wickets</a>&nbsp;or exhausts all its overs, then the game is declared a&nbsp;<em>tie</em>&nbsp;(regardless of the number of wickets lost by either team).</p>
                </div>
                <p align="justify" class="style49">&nbsp;</p>
                <p align="justify" class="style49">* DUCKWORTH-LEWIS METHOD: </p>
                <p align="justify" class="style44">Where a number of overs are lost, for example, due to inclement weather conditions, then the total number of overs may be reduced; when this occurs, the target in the run change, or the overall result may be determined by the&nbsp;<a href="http://en.wikipedia.org/wiki/Duckworth-Lewis_method" title="Duckworth-Lewis method">Duckworth-Lewis method</a>. Where insufficient overs are played to apply the Duckworth-Lewis method, a match is declared no result. Important one-day matches, particularly in the latter stages of major tournaments, may have two days set aside, such that a result can be achieved on the &quot;reserve day&quot; if the first day is washed out &ndash; either by playing a new game, or by resuming the match which was rain-interrupte</p>
                <p align="justify" class="style42">&nbsp;</p>
                <p align="justify" class="style48">* FIELDING RESTRICTIONS AND POWERPLAY:</p>
                <p align="justify" class="style51">-&gt; POWERPLAY:</p>
                <p align="justify" class="style43"><span class="style44">Powerplay in one day cricket has been confusing fans around the world ever since it was&nbsp;<a href="http://www.itsonlycricket.com/entry/106/">first introduced in 2005</a>and on 1 October 2011 onwards, few clauses were added. From December 2012, ICC has re-revised the rule once again. here's how the new power play rule stands..<br />
                </span><span class="style44"><br />
                  An ODI has two innings, of 50 over each, in which first 10 overs are mandatory field restricted which means no more than 2 fielders can be outside the 30 yard circle and also, two fielders HAVE to be at catching positions.<br />
                  <br />
                  Previously, two blocks of 5 overs, known as batting powerplay and bowling powerplay, opted by batting and fielding side respectively, was the rule but now,&nbsp;<strong>no bowling powerplay</strong>&nbsp;is required, just the batting powerplay which should be completed before 40th over. Once called for, no more than 3 fielders will be outside 30 yard circle but this time, no catching fielder is required although fielding captain may well choose to.<br />
                  <br />
                  Note that if there's no power play in progress,&nbsp;<strong>maximum of 4 fielders</strong>&nbsp;can patrol the boundary in one day internationals.<br />
                  <br />
                  More over, each bowler now has&nbsp;<strong>two bouncers</strong>&nbsp;each over. Now, what was happening in first 7 years since this rule first came into picture is that captains were not experimenting enough. Fielding sides were calling their bowling powerplays straight after first 10 overs to limit the damage in case batting side if off to flying start and batting side often called their batting powerplay as late as possible so that they can collect more runs freely.<br />
                  <br />
                  October 2011 onwards, ICC has twicked the rule a bit by forcing captains to call batting and bowling powerplays inside 16th to 40th over. This means once first 10 overs of mandatory field restriction is over, bowling side can't call for PP. If they want to, they'll have to wait till completion of 15th over. On other hand, batting side will have to call batting PP on or before 36th over. This means overs 11th to 15 and 40th to 50th cannot be under powerplays!<br />
                  <br />
                  Note that this rule applys only if the innings is off full 50 overs. In case of rain or other factors for which match has been reduced below 40 overs an innings, new changes won't be applied.&nbsp;<br />
