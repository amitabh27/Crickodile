          <p align="center"><em class="style3"><strong>FOURTH SEASON OF INDIAN PREMIER LEAGUE (2011) : </strong></em></p>
          <p align="left">On 21 March 2010, it was announced in Chennai that two new teams from Pune and Kochi will be added to the IPL for the fourth season. However, the bid around   the kochi franchise turned controversial resulting in the resignation   of minister, Shashi Tharoor from the Central Government and investigations by various departments   of the Government of India into the financial dealings of IPL and the   other existing franchises. </p>
          <p align="left">The addition of teams representing Sahara Pune Warriors India and the   Kochi Tuskers was to have increased the number of franchises from 8 to   10. The BCCI originally considered extending the tournament format used   in previous season to ten teams, which would increase the number of   matches from 60 to 94. Instead, the round-robin stage of the tournament   was to have been replaced by a group stage with two groups of five,   limiting the number of matches to 74. But this format was replaced by another one in which each team would   play 5 other teams in a two-way round robin format and there would be 2   teams against whom they would play only at home and remaining 2 teams   against which they would play only away matches.Thus each team plays 14   matches.Top four teams would qualify for the semi-finals.</p>
          <p align="left"><strong>Chennai Super Kings</strong> won their second consecutive title after defeating the Royal Challengers Bangalore by 58 runs in the final. This is the first time a franchise has won two titles. The top four teams qualified for the 2011 Champions League Twenty20, which the Mumbai Indians won.</p>
