              <td width="998" align="left" valign="top"><p align="justify">&nbsp;</p>
                <blockquote>
                  <p align="justify"><span class="style10"><strong>Ranji Trophy</strong>&nbsp;is a domestic&nbsp;first-class cricket&nbsp;championship played in&nbsp;India between teams representing regional cricket associations, equivalent to the County Championship&nbsp;in England and the&nbsp;Sheffield Shield&nbsp;in&nbsp;Australia. The competition is named after England and Yorkshire cricketer&nbsp;Kumar Shri Ranjitsinhji&nbsp;(Jam Sahib&nbsp;of&nbsp;Nawanagar, also known as &quot;Ranji&quot;). The 2013 Ranji Trophy was won by Mumbai, who won their 40th title by defeating Saurashtra by an inning and 125 runs on 28 January 2013.</span></p>
                </blockquote>
                <p align="left">&nbsp;</p>
                <blockquote>
                  <p align="left" class="style11">* HISTORY : </p>
                  <p align="justify" class="style10">The competition was launched as &quot;The Cricket Championship of India&quot; following a meeting of the Board of Control for Cricket in India&nbsp;in July 1934, with the first fixtures taking place in&nbsp;1934&ndash;35. The trophy was donated by&nbsp;Maharaja Bhupinder Singh of Patiala. The first Ranji Trophy Championship was won by&nbsp;Bombay&nbsp;after they defeated North India in the final.&nbsp;Syed Mohammed Hadi&nbsp;of Hyderabad&nbsp;was the first batsman to score a hundred.</p>
                  <p align="justify" class="style10">Mumbai (formerly Bombay) have been the dominant team in the Championship so far, with 40 wins, including 15 back-to-back wins from 1958&ndash;59&nbsp;to&nbsp;1972&ndash;73.</p>
                  <p align="left">&nbsp;</p>
                  <p align="left" class="style11">* PARTICIPANTS: </p>
                  <p align="justify" class="style10">State teams and cricket associations and clubs with first-class status are qualified to play in the Ranji Trophy. While most association are regional, like the&nbsp;Tamil Nadu Cricket Association&nbsp;and&nbsp;Mumbai Cricket Association&nbsp;some are pan-Indian like Railways and Services.</p>
                  <p align="justify" class="style10">&nbsp;</p>
                  <p align="justify" class="style13">* DEFUNCT RANJI TEAMS: </p>
                  <p align="justify" class="style10">The following sides have appeared in Ranji Trophy, but no longer do so: Southern Punjab (1934/35-1967/68), Sind (1934/35-1947/48), Northern India (1934/35-1946/47), Central Provinces &amp; Berar (1934/35-1949/50), Western India (1934/35-1945/46), North West Frontier Province (1937/38-1946/47), Holkar (1941/42-1954/55) Gwalior (1943/44), Kathiawar (1946/47-1949/50), Patiala/Patiala and Eastern Punjab States Union (1948/49 , 1953/54-1958/59), Eastern Punjab (1950/51-1959/60), Madhya Bharat (1955/56-1956/57), Northern Punjab (1960/61-1967/68).</p>
