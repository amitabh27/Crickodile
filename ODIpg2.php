                <p class="style16">RULES:</p>
                <blockquote>
                  <p align="justify"> I<span class="style19">n ODIs, each team gets to bat only a fixed number of overs. In the early days of ODI cricket, the number of overs was generally 60 overs per side, and matches were also played with 40, 45 or 55 overs per side, but now it has been uniformly fixed at 50 overs.</span></p>
                  <p align="justify" class="style19">Simply stated, the game works as follows:</p>
                  <div align="justify">
                    <ul>
                      <li class="style19">An ODI is contested by 2 teams of 11 players each.</li>
                      <li class="style19">The Captain of the side winning the toss chooses to either bat or bowl (field) first.</li>
                      <li class="style19">The team batting first sets the target score in a single innings. The innings lasts until the batting side is &quot;all out&quot; (i.e., 10 of the 11 batting players are &quot;out&quot;) or all of the first side's allotted overs are completed.</li>
                      <li class="style19">Each bowler is restricted to bowling a maximum of 10 overs (fewer in the case of rain-reduced matches and in any event generally no more than one fifth or 20% of the total overs per innings). Due to this, each team must comprise at least five competent bowlers (either dedicated bowlers or all-rounders).</li>
                      <li class="style19">The team batting second tries to score more than the target score in order to win the match. Similarly, the side bowling second tries to bowl out the second team or make them exhaust their overs before they reach the target score in order to win.</li>
                      <li class="style19">If the number of runs scored by both teams is equal when the second team loses all of its wickets or exhausts all its overs, then the game is declared a&nbsp;<em>tie</em>&nbsp;(regardless of the number of wickets lost by either team).</li>
                    </ul>
                  </div>
                  <p align="justify" class="style17">&nbsp;</p>
                  <p align="justify" class="style17">&nbsp;</p>
                  <p align="justify" class="style17">* DUCKWORTH-LEWIS METHOD: </p>
                  <blockquote>
                    <p align="justify" class="style18">Where a number of overs are lost, for example, due to inclement weather conditions, then the total number of overs may be reduced; when this occurs, the target in the run change, or the overall result may be determined by the Duckworth-Lewis Method. Where insufficient overs are played to apply the Duckworth-Lewis method, a match is declared no result. Important one-day matches, particularly in the latter stages of major tournaments, may have two days set aside, such that a result can be achieved on the &quot;reserve day&quot; if the first day is washed out &ndash; either by playing a new game, or by resuming the match which was rain-interrupted.</p>
