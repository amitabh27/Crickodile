                <p class="style24">ODI RECORDS:                </p>
                <p class="style24">&nbsp;</p>
                <blockquote>
                  <p align="justify" class="style19">The record for the highest innings total in any List A limited overs match is 443 for nine by Sri Lanka against Netherlands in their One Day International 50-overs match at Amstelveen on 4 July 2006. The lowest team total is 35 all out by Zimbabwe against Sri Lanka in Harare, 2004.</p>
                  <p align="justify" class="style19">The most runs scored by both sides in any List A limited overs match is 872: Australia, batting first, scored 434 for four in 50 overs (at the time the highest ever first innings total), and yet they were beaten by South Africa who scored 438 for nine with a ball to spare during their ODI at Johannesburg in 2006. </p>
                  <p align="justify" class="style19">The best bowling figures are 8&ndash;19 by Chaminda Vaas &nbsp;for Sri Lanka v Zimbabwe in Colombo, 2001&ndash;02 &ndash; he is the only player to take eight wickets in a One Day international. The best batting performance is a knock of 219 runs by Indian Virender Sehwag against the West Indies on 8 December 2011. He was the first captain and the second person so far to score a double century in One Day Internationals with the other being Indian batsman Sachin Tendulkar &nbsp;on 24 Feb 2010 who made the first ever one day double century off 147 balls against South Africa.</p>
